<script>
    const {
        createApp
    } = Vue

    createApp({
        data() {
            return {
                counter: '{{ $cartCount ?? 0 }}',
                cartItems: [],
                totalCheckout: 0
            };
        },
        methods: {
            addToCart(id) {
                let quantity = 1;
                axios.post(`{{ route('add-product') }}`, {
                    product_id: id,
                    quantity: quantity
                }).then((res) => {
                    if (res.status == 200) {
                        new Noty({
                            type: 'success',
                            layout: 'topRight',
                            text: res.data.message,
                            timeout: 2000,
                            kill: true
                        }).show();

                        this.counter = res.data.cartCount;

                    }
                });
            },
            getCart() {
                axios.get(`{{ route('cart') }}`, {}).then((res) => {
                    if (res.status == 200) {

                        this.cartItems = res.data.cartItems;
                        this.totalCheckout = res.data.totalCheckout;

                    }
                });
            },
            cartRemove(id) {
                axios.post(`{{ route('cart-delete') }}`, {
                    itemId: id
                }).then((res) => {
                    if (res.status == 200) {

                        new Noty({
                            type: 'success',
                            layout: 'topRight',
                            text: res.data.message,
                            timeout: 2000,
                            kill: true
                        }).show();
                        this.counter = res.data.cartCount;

                    }
                });
            },


        },
    }).mount('#app')
</script>
